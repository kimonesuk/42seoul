#include "libft.h"

int	ft_atoi(const char *s)
{
	int	rtn;
	int	sign;

	rtn = 0;
	sign = 1;
	if (s[0] == 43)
		s++;
	else if (s[0] == 45)
	{
		s++;
		sign = -1;
	}
	while (*s)
	{
		if (*s >= 48 && *s <= 57)
			rtn = rtn * 10 + *s - 48;
		else
			break ;
		s++;
	}
	return (sign * rtn);
}
