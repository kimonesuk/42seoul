#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (*((char *)s + sizeof(char) * i) == (char)c)
			return ((char *)s + sizeof(char) * i);
		i++;
	}
	return (0);
}
