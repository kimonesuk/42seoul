#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return ((char *)s + sizeof(char) * i);
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)s + sizeof(char) * i);
	return (0);
}
